let numero = 7; // aqui criei uma variável para guardar o número da tabuada

for (let i = 1; i <= 10; i++) {
    // o for cria um laço de repetição de 1 a 10

    // i começa em 1 porque defini esse valor como início do loop
    // enquanto i for menor ou igual a 10 o loop continua
    // depois de cada volta, i aumenta 1

    console.log(numero + " x " + i + " = " + (numero * i));
    // aqui ele mostra a multiplicação do número pelo contador
}